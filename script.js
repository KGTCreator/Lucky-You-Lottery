function clearName(){
	if (document.getElementById('inputFirstName3').value == 'First and Last Name'){
	document.getElementById('inputFirstName3').value = '';
	} 
}

function resetName(){
	if (document.getElementById('inputFirstName3').value == ''){
	document.getElementById('inputFirstName3').value = 'First and Last Name';
	} 
}

function clearPhone(){
	if (document.getElementById('inputLastName3').value == 'Wakeman'){
	document.getElementById('inputLastName3').value = '';
	} 
}

function resetPhone(){
	if (document.getElementById('inputLastName3').value == ''){
	document.getElementById('inputLastName3').value = 'Wakeman';
	} 
}